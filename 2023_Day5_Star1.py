def getLowestLocation(seeds, mappingsList):
    lowestLocation = int(seeds[0])
    for seed in seeds:
        search_seed = int(seed)
        for mapType in mappingsList:
            for mapping in mapType:
                dest_start, sour_start, step = map(int, mapping.strip().split(' '))
                sour_end = sour_start + step - 1
                if search_seed >= sour_start and search_seed <= sour_end:
                    diff = search_seed - sour_start
                    search_seed = dest_start + diff
                    break
        lowestLocation = min(lowestLocation, search_seed)
    return lowestLocation

